<template>
  <div id = "button">
    <v-btn
      color='teal'
      dark
      depressed
      fab
      @click="toggle"
    >
      <v-icon 
        v-if="isPlaying"
        large
      >pause</v-icon>
      <v-icon
        v-else
        large
      >play_arrow</v-icon>
    </v-btn>
  </div>
</template>
<script>
import { eventBus } from "../../main";
export default {
  data () {
    return {
      isPlaying: false
    }
  },

  methods: {
    toggle () {
      this.isPlaying = !this.isPlaying
      eventBus.$emit("toggle-timelapse", this.isPlaying)
    }
  }
}
</script>
